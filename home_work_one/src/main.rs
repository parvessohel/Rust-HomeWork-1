fn main() {
    let x: i32; // Variable x is declared with Type annotation that is i32. It is an unsigned integer.
    x = 5; // Variable x is assigned with value 5. It works because it is declared before but wasn't assigned any value.
    println!("The value of X is {}", x); // Variable x is printed with the help of println! macro and {}.

    // let x: i32 = 5; It can also be initialized and assiged at the same time with Type annotation attached to it.
}

